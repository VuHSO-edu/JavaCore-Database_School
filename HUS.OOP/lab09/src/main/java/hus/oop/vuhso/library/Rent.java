public class Rent {

    Item item;
    Student student;
    Date begin;
    Date end;

    public Item getItem() {
        return this.item;
    }

    public void setItem(Item item) {
        this.item = item;
    }

    public Student getStudent() {
        return this.student;
    }

    public void setStudent(Student student) {
        this.student = student;
    }

    public Date getBegin() {
        return begin;
    }

    public void setBegin(Date begin) {
        this.begin = begin;
    }

    public Date getEnd() {
        return this.end;
    }

    public void setEnd(Date end) {
        this.end = this.end;
    }

    public Rent(Item item, Student student, Date begin, Date end) {
        this.item = item;
        this.student = student;
        this.begin = begin;
        this.end = end;
    }

    @Override
    public String toString() {
        return "Rent[" +
                "item=" + this.item +
                ", student=" + this.student +
                ", begin=" + this.begin +
                ", end=" + this.end +
                ']';
    }

}
